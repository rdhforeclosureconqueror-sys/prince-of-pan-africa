export function findContainerGuideEntryForTask(task) {
  const label = `${task?.source_chapter_label || task?.linked_handbook_chapter || ""} ${task?.linked_container_step || ""}`;
  const number = Number((label.match(/Chapter\s+(\d+)/i) || [])[1]);
  if (number) return containerGuideEntries.find((entry) => entry.chapterNumber === number);
  const title = (task?.title || "").toLowerCase();
  return containerGuideEntries.find((entry) => entry.connectedTasks.some((taskTitle) => title.includes(taskTitle.toLowerCase()) || taskTitle.toLowerCase().includes(title))) || null;
}
